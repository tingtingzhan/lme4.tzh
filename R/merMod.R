#' @rdname s3_merMod
#' @importFrom lme4 methTitle
#' @importFrom ecip desc_
#' @export desc_.merMod
#' @export
desc_.merMod <- function(x) {
  # see inside ?lme4:::print.merMod
  x@devcomp$dims |> 
    methTitle() |>
    gsub(pattern = ' fit by .*$', replacement = '') |> 
    tolower()
}



