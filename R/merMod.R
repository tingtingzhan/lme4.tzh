
#' @title S3 Method Dispatches for \link[lme4]{merMod} Class
#' 
#' @description
#' ..
#' 
#' @param x a \link[lme4]{merMod} object
#' 
#' @param level \link[base]{double} scalar, confidence level
#' 
#' @param method ..
#' 
#' @param ... ..
#' 
#' @examples
#' # see ?lme4::glmer or ?lme4::cbpp
#' library(lme4)
#' class(m1 <- glmer(cbind(incidence, size - incidence) ~ period + (1 | herd), 
#'   data = cbpp, family = binomial))
#' desc_.glmerMod(m1)
#' coef_.merMod(m1)
#' confint_.merMod(m1)
#' nobsText.merMod(m1)
#' 
#' startvec = c(Asym = 200, xmid = 725, scal = 350)
#' (m2 <- nlmer(circumference ~ SSlogis(age, Asym, xmid, scal) ~ Asym|Tree,
#'   Orange, start = startvec))
#' class(m2)
#' desc_.merMod(m2)
#' 
#' @keywords internal
#' @name s3_merMod
#' @importFrom ecip .pval
#' @method .pval summary.merMod
#' @export .pval.summary.merMod
#' @export
.pval.summary.merMod <- function(x) {
  # ('glmerMod' inherits from 'merMod')
  # ('nlmerMod' inherits from 'merMod')
  # returned value from ?lme4:::summary.merMod
  cf <- x$coefficients
  ret <- cf[, 'Pr(>|z|)'] # has error on 'nlmerMod' object!!
  names(ret) <- rownames(cf)
  return(ret)
}


#' @rdname s3_merMod
#' @examples
#' library(lme4)
#' library(HSAUR3)
#' m = glmer(outcome ~ treatment + visit + (1|patientID), data=toenail, family=binomial, nAGQ=20)
#' m |> vterms.merMod()
#' @importFrom stats formula terms
#' @importFrom ecip vterms
#' @export vterms.merMod
#' @export
vterms.merMod <- function(x) {
  
  tmp. <- x |>
    terms() |> # ?lme4:::terms.merMod returns only fixed effect!!
    attr(which = 'variables', exact = TRUE) |>
    as.list.default()
  ret <- tmp.[-1L] # remove first element of quote(list)
  
  # ?lme4:::formula.merMod
  ranfom <- x |> 
    formula(random.only = TRUE)
  
  attr(ret, which = 'group') <- ranfom[[3L]]
  return(ret)
  
}






